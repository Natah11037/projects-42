#include "libft.h"

int	ft_putmem(char *s)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	if (s == NULL)
		return (i);
	while (s[i] != '\0')
	{
		if (i == 1)
			j += write(1, "x", 1);
		write(1, &s[i], 1);
		i++;
		j++;
	}
	return (j);
}
