#include "ft_printf.h"

int	ft_putmem(char *s)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	if (s == NULL)
	{
		j += write(1, "(nil)", 5);
		return (j);
	}
	j += write(1, "0x", 2);
	while (s[i] != '\0')
	{
		write(1, &s[i], 1);
		i++;
		j++;
	}
	return (j);
}
